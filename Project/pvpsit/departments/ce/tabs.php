<div class="tabContent" id="about">
  <h2>
    About Department
  </h2>
  <div>
    <p>The PVP Siddhartha Institute of Technology has started offering B. Tech program in Civil Engineering only in 2008. The Department of Civil Engineering is in the process of building up faculty and laboratories. The Department has well equipped laboratories in the fields of Surveying, Fluid Mechanics, Strength of Materials, Engineering Geology and Environmental Engineering, Geo Technical engineering, Highway engineering, concrete technology, CAD and RS & GIS with basic equipment required for undergraduate courses.</p>

    <p>Faculty members are encouraged to upgrade their academic qualification and gain experience by attending trainings, workshops, conferences etc. THREE faculty members posses PhD degrees from reputed universities/institutions and THREE faculty member has registered for Ph.D. To meet the requirements of the industry the students were trained in the usage of total station (survey equipment), and latest software in structural design, AutoCAD, STAAD-Pro, Arc GIS, SUPER AUTO ESTIMATOR etc. The students are also encouraged to attend workshops, conferences etc. and present papers and also for participation in other extracurricular activities like cultural events, NSS, NCC, sports and games etc.</p>

    <p>A Civil Engineering association has been formed and the association organizes functions like World Water Day, Environmental awareness program etc. The Department organizes guest lectures by eminent personalities and subject experts.</p>

    <p>The Department also doing consultancy works in various areas as follows.</p>

    <ul>
      <li>Structural Engineering</li>
      <li>Material Testing</li>
      <li>Mix Design</li>
      <li>Transportation engineering</li>
      <li>Environmental engineering</li>
      <li>Geo Technology and Foundation Engineering</li>
    </ul>
  </div>
</div>

