      </h2>
      <div>
      <p>Welcome to one of the largest departments of PVPSIT which offers U.G and P.G programs in Electronics and Communication Engineering. The department has been playing a key role in producing scientists and technologists of higher caliber since its establishment in the year 2000. The department offers B.Tech and M.Tech programs to cater the challenging needs of technical excellence in all areas of Electronics and Communication Engineering.</p>

      <p>In addition to the strong undergraduate program, the department has been playing a pioneering role in producing postgraduates for teaching and rescearch. The infrastructure and lab facilities are upgraded from time to time and provide adequate opportunities for students and researchers to innovate new ideas. The department has distinguished faculty and all the faculty members are consistently carrying out research on cutting edge technologies. The department also undertakes research projects sponsored by both the government and the industry. </p>
