<h2>
About Department
</h2>
<div>
<p>EEE department was started with the annual intake of 60 at UG level in the year 2001. The intake was increased to 90 in the year 2007, and was further increased to 120 in the year 2011. EEE department has a carpet area of 2074sq. m. The cost of the equipment in the laboratories is about Rs. 1,19,79,925.</p>

<p>The Department is accredited by the AICTE, National Board of Accreditation in 2007.The Department is certified by ISO 9001:2008 certification. It has well established instructional facilities. It has 12 laboratories viz. Electrical Machines Lab-I & II, Networks Lab, Electrical Technology Lab, Electrical Measurements Lab, Power Electronics Lab, Electronic devices and circuits lab, Control Systems Lab, Simulation of Electrical Systems lab, Micro processor and Microcontrollers lab, Power Systems Lab & Calibration Lab. The department has thirty faculty members and out of which two are Professors, five are Associate professors, two are Sr. Assistant professors and twenty one are Assistant Professors. They are actively engaged in research work. Seven of them are pursuing their Ph.D programe. Department organized programmes like Alumni interactive sessions with current students, TECHNOFEST, Energy conservation day etc. To meet the requirements of the industry, the students were trained in the latest softwares- MAT Lab, PSCAD, PSIM, PSPICE, Mi Power etc. The students are also encouraged to attend seminars, workshops, conferences etc. They are encouraged to participate in extra circular activities like cultural events, NSS, NCC, sports and games etc.</p>

<p>The department has entered into MOU with Dr.NTTPS Training Institute for industrial training to EEE students. The training institute is the category - 1 institute by Central Electricity Authority, New Delhi. </p>
</div>
