        <p>

          The Department of Computer Science and Engineering was formed in the year 1999. The department has started B.Tech program with an initial intake of 40 in the year 1999; and 120 from 2007 onwards. The department has also started M.Tech program with an initial intake of 18 in the year 2006, and an increase in the intake to 36 in the year 2011. The department is accredited by NBA in the year 2007 for a period of 3 years and was reaccredited in the year 2012 for a period of 2 years. The department has 37 qualified teaching staff with 3 PhD and 32 M.Techâ€™s. Our faculty members aim at delivering top class education blending their rich research experience with class room teaching. The entire faculty is involved in research activities and has published/presented 200 papers in national and international journals and conferences.
        </p>

        <p>
          The department has state-of-the-art infrastructure and computing equipment worth Rs. 1.8 cr supported by high speed 60 mbps Internet and wireless networks. To provide a flavor of different platforms, the department has established a Sun Solaris Laboratory, Open Source Laboratory with Ubuntu OS and an Incubation (R&D) Center. The department also has ACM student chapter and CSI student chapter. The department also offers various certification training programs to students like CISCO: CCNA certification, Oracle certification under WDP, and Sun Java certification under SAI to make the students well prepared to face the industry. The department has also established Intel FICE Lab to encourage students with new technologies.
